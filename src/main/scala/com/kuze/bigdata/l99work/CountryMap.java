package com.kuze.bigdata.l99work;

public class CountryMap {

    private String continent_cname;
    private String continent_name;
    private String country_cname;
    private String country_code;
    private String country_name;

    public String getContinent_cname() {
        return continent_cname;
    }

    public void setContinent_cname(String continent_cname) {
        this.continent_cname = continent_cname;
    }

    public String getContinent_name() {
        return continent_name;
    }

    public void setContinent_name(String continent_name) {
        this.continent_name = continent_name;
    }

    public String getCountry_cname() {
        return country_cname;
    }

    public void setCountry_cname(String country_cname) {
        this.country_cname = country_cname;
    }

    public String getCountry_code() {
        return country_code;
    }

    public void setCountry_code(String country_code) {
        this.country_code = country_code;
    }

    public String getCountry_name() {
        return country_name;
    }

    public void setCountry_name(String country_name) {
        this.country_name = country_name;
    }
}
